def generate_secret_key(filename):
    from django.utils.crypto import get_random_string
    chars = 'abcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*(-_=+)'
    seckey = get_random_string(50, chars)
    f = open(filename, 'w')
